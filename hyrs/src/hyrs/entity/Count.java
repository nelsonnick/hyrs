package hyrs.entity;

public class Count {
	private long id;
	private String name;
	private long all;
	private long add;
	private long alter;
	private long reduce;
	private long retire;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public long getAll() {
		return all;
	}
	public void setAll(long all) {
		this.all = all;
	}
	public long getAdd() {
		return add;
	}
	public void setAdd(long add) {
		this.add = add;
	}
	public long getAlter() {
		return alter;
	}
	public void setAlter(long alter) {
		this.alter = alter;
	}
	public long getReduce() {
		return reduce;
	}
	public void setReduce(long reduce) {
		this.reduce = reduce;
	}
	public long getRetire() {
		return retire;
	}
	public void setRetire(long retire) {
		this.retire = retire;
	}
	
	
}
